# Arithmetic operators used with numeric values to perform common mathematical operations

# + , -, , /, % , //, *

num1=int(input('Enter number1'))
num2=int(input('Enter number2'))

print('Add',num1+num2)
print('Sub',num1-num2)
print('Multiplication',num1*num2)
print('Division',num1/num2)
print('Modulus',num1%num2) # to get the reminder
print('Floor Division',num1//num2) # to remove the decimal part of the number
print('Exponent',num1**num2) # to calculate the power of a number