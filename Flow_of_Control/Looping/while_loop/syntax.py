'''' With the while loop we can execute a set of statements as long as a condition is true.

Note: remember to increment i, or else the loop will continue forever.
'''

# syntax
# while condition:   #in while loop we have to write the code for incrementation
    # code

# Examples:
# print 1 to 10
num=1
while num<=10:
    print(num)
    num=num+1
print('out',num)

# print 10 to 1
num=10
while num>=1:
    print(num)
    num=num-1

# print 2,4,6,8
num=2
while num<=8:
    print(num)
    num=num+2

